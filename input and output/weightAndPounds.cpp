/*
 * working with float
 * asking for user weight and pounds (weightAndPounds.cpp)
 */
 #include<iostream>
 using namespace std;

 int main()
 {
 int age;
 float pounds;

 // INPUT AND output
 cout << "Enter your age: ";
 cin >> age;
 cout << "How much do you weight: ";
 cin >> pounds;

 // DISPLAY RESULTS
 cout << "\nYour age is " << age << " and you weight is " << pounds << " pounds." << endl;

 return 0;
 }
